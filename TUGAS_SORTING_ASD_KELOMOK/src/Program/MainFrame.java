    private void sortbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortbuttonActionPerformed
        Sorting sort = new Sorting();

        String selectedSort = comboBox.getSelectedItem().toString();
        boolean sortAscending = ascRadioButton.isSelected();
        int[] array = inputArray();

        if(buttonGroup1.getSelection() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select the ascending or descending option before!");
        } else if (array != null) {
            switch (selectedSort) {
                case "Bubble Sort":
                    sort.bubbleSort(array, sortAscending);
                    break;
                case "Insertion Sort":
                    sort.insertionSort(array, sortAscending);
                    break;
                case "Selection Sort":
                    sort.selectionSort(array, sortAscending);
                    break;
                case "Merge Sort":
                    sort.mergeSort(array, 0, array.length - 1, sortAscending);
                    break;
                case "Shell Sort":
                    sort.shellSort(array, array.length, sortAscending);
                    break;
            }
            displayArray(array);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please input a valid integer value!");
        }
    }//GEN-LAST:event_sortbuttonActionPerformed

    public int[] inputArray() {
        int[] array = new int[10];
        try {
            array[0] = Integer.parseInt(b1.getText());
            array[1] = Integer.parseInt(b2.getText());
            array[2] = Integer.parseInt(b3.getText());
            array[3] = Integer.parseInt(b4.getText());
            array[4] = Integer.parseInt(b5.getText());
            array[5] = Integer.parseInt(b6.getText());
            array[6] = Integer.parseInt(b7.getText());
            array[7] = Integer.parseInt(b8.getText());
            array[8] = Integer.parseInt(b9.getText());
            array[9] = Integer.parseInt(b10.getText());
        } catch (NumberFormatException e) {
            return null;
        }
        return array;
    }

